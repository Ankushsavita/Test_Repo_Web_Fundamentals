//Question 1. Explain difference between var/ let const by giving examples.

//var:- 
var d = 10;
console.log(d);
                     //we can re-declare the variable.
var j = 84.5;        //we can re-initialize the value.
console.log(j);      //initialization and declaration can be done in new line.

//let :- 
let a = 50;
console.log(a);

n = 60;
console.log(n);       //we can re-initialize the value.
                      //But we can not re-declare the variable.
_class = 70;          //initialization and declaration can be done in new line.
console.log(_class);

//const:-          
const pi = 3.14;     //we can not re-declare the variable.
console.log(pi);     //we can not re-initialize the value also.
                     // But here, initialization and declaration should be done in same line.
